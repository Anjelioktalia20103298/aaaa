    private void initTable(){
        model = new DefaultTableModel();
        tDSN.setModel(model);
        model.addColumn("NIDN");
        model.addColumn("Nama Dosen");
        model.addColumn("Alamat");
    }
    private void tampildata(String sql){
        try{
            Statement stmt = cnn.koneksi().createStatement();
            this.rs = stmt.executeQuery(sql);
        }catch(Exception e){
            System.out.println("Terjadi Kendala pada query");
        }
    }
    private void getdata(){
        String sql = "Select NIDN, NAMA_DOSEN, ALAMAT FROM dosen;";
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try{
            this.tampildata(sql);
            while(this.rs.next()){
                Object[] obj = new Object[3];
                obj[0] = this.rs.getString("NIDN");
                obj[1] = this.rs.getString("NAMA_DOSEN");
                obj[2] = this.rs.getString("ALAMAT");
                model.addRow(obj);
            }
        }catch(Exception e){
        }
    }
    private boolean UpdateData(String SQL){
        boolean hsl = false;
        try{
            Statement stmt = cnn.koneksi().createStatement();
            stmt.executeUpdate(SQL);
            hsl = true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Terjadi kendala pada instruksi SQL");
        }
        return hsl;
    }

