    private void cmdDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDeleteActionPerformed
        String NIDN = this.txNIDN.getText();
        String NAMA_DOSEN = this.txND.getText();

        int opsi = JOptionPane.showConfirmDialog(null,"Yakin akan menghapus data dari "+NAMA_DOSEN+"("+NIDN+")?","Konfirmasi", JOptionPane.YES_NO_OPTION );
        if(opsi == JOptionPane.YES_OPTION){
            String SQLDelete = "DELETE FROM dosen WHERE NIDN='"+NIDN+"';";
            if(this.UpdateData(SQLDelete)){
                this.getdata();
                JOptionPane.showMessageDialog(null, "Data telah dihapus");
            }
        }
    }//GEN-LAST:event_cmdDeleteActionPerformed

    private void tDSNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tDSNMouseClicked
        String urlGambarNull = "src/img/gambar.jpg";
        this.txNIDN.setText( model.getValueAt(tDSN.getSelectedRow(), 0).toString());
        this.txND.setText( model.getValueAt(tDSN.getSelectedRow(), 1).toString());
        this.txAD.setText( model.getValueAt(tDSN.getSelectedRow(), 2).toString());
        String urlGambar = "src/img/"+this.txNIDN.getText() + ".jpg";
        BufferedImage loadImg = img.loadImage(urlGambar);
        if(loadImg == null){
            loadImg = img.loadImage(urlGambarNull);
        }
        ImageIcon imageIcon = new ImageIcon(loadImg);
        this.imgphoto.setIcon(imageIcon);

        this.lbCURD.setText("Edit Data "+this.txNIDN.getText());
        this.cmdStore.setEnabled(false);
        this.cmdUpdate.setEnabled(true);
        this.cmdDelete.setEnabled(true);
    }//GEN-LAST:event_tDSNMouseClicked

